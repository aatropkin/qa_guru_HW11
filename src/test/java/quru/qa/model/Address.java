package quru.qa.model;

public class Address {
    private String city;
    private int index;
    private String street;
    private int house;

    public String getCity() {
        return city;
    }

    public int getIndex() {
        return index;
    }

    public String getStreet() {
        return street;
    }

    public int getHouse() {
        return house;
    }
}
